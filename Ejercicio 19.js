/*

Realizar un programa que pida al usuario un número y le indique si es múltiplo de 3 o no.
Cuando decimos que un número es múltiplo de otro, 
significa que podemos obtener el primer número al multiplicar el segundo número por algún entero.

*/

let number = parseInt(prompt("Ingrese el numero"))

if (number / 3 === 0) {
    console.log("Es multiplo de 3")
} else {
    console.log("No es multiplo de 3")
}




