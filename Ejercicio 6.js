// Calcular el area y perimetro de un rectangulo

let l1 = prompt("Ingrese el ancho del rectangulo ")
let l2 = prompt("Ingrese la altura del rectangulo ")

let area = l1 * l2 //Calculo de area
let perimetro = (l1*2) + (l2*2) //Calculo de perimetro

document.write("El area es " + area + " mientras que el perimetro es " + perimetro)
